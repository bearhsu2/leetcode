package idv.kuma.medium.maximum_difference_between_node_and_ancestor;

public class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;


    TreeNode() {
    }


    TreeNode(int val) {
        this.val = val;
    }


    TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
